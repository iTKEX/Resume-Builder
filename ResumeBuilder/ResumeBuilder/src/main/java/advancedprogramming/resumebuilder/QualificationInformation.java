package advancedprogramming.resumebuilder;

/**
 *
 * @author tkex_
 */
public class QualificationInformation {

    String firstQualificationProgram, firstQualificationGrade, firstQualificationPassingYear,
            secondQualificationProgram, secondQualificationGrade, secondQualificationPassingyear,
            thirdQualificationProgram, thirdQualificationGrade, thirdQualificationPassingYear;

    public QualificationInformation() {
        
        this.firstQualificationProgram = "";
        this.firstQualificationGrade = "";
        this.firstQualificationPassingYear = "";
        
        this.secondQualificationProgram = "";
        this.secondQualificationGrade = "";
        this.secondQualificationPassingyear = "";
        
        this.thirdQualificationProgram = "";
        this.thirdQualificationGrade = "";
        this.thirdQualificationPassingYear = "";
        
    }

    public String getFirstQualificationProgram() {
        return firstQualificationProgram;
    }

    public void setFirstQualificationProgram(String firstQualificationProgram) {
        this.firstQualificationProgram = firstQualificationProgram;
    }

    public String getFirstQualificationGrade() {
        return firstQualificationGrade;
    }

    public void setFirstQualificationGrade(String firstQualificationGrade) {
        this.firstQualificationGrade = firstQualificationGrade;
    }

    public String getFirstQualificationPassingYear() {
        return firstQualificationPassingYear;
    }

    public void setFirstQualificationPassingYear(String firstQualificationPassingYear) {
        this.firstQualificationPassingYear = firstQualificationPassingYear;
    }

    public String getSecondQualificationProgram() {
        return secondQualificationProgram;
    }

    public void setSecondQualificationProgram(String secondQualificationProgram) {
        this.secondQualificationProgram = secondQualificationProgram;
    }

    public String getSecondQualificationGrade() {
        return secondQualificationGrade;
    }

    public void setSecondQualificationGrade(String secondQualificationGrade) {
        this.secondQualificationGrade = secondQualificationGrade;
    }

    public String getSecondQualificationPassingyear() {
        return secondQualificationPassingyear;
    }

    public void setSecondQualificationPassingyear(String secondQualificationPassingyear) {
        this.secondQualificationPassingyear = secondQualificationPassingyear;
    }

    public String getThirdQualificationProgram() {
        return thirdQualificationProgram;
    }

    public void setThirdQualificationProgram(String thirdQualificationProgram) {
        this.thirdQualificationProgram = thirdQualificationProgram;
    }

    public String getThirdQualificationGrade() {
        return thirdQualificationGrade;
    }

    public void setThirdQualificationGrade(String thirdQualificationGrade) {
        this.thirdQualificationGrade = thirdQualificationGrade;
    }

    public String getThirdQualificationPassingYear() {
        return thirdQualificationPassingYear;
    }

    public void setThirdQualificationPassingYear(String thirdQualificationPassingYear) {
        this.thirdQualificationPassingYear = thirdQualificationPassingYear;
    }
    
}
